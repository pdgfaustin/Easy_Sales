    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            easy_sales.connexionEasy();
            easy_sales.Pst = (PreparedStatement) easy_sales.cn.clientPrepareStatement("SELECT idArticles,qlteArticle,idSite,semaineApp, qteLundi,qteMardi, "
                    + "qteMercredi, qteJeudi, qteVendredi, qteSamedi, qteDimanche FROM approSemaine WHERE idSite = ? AND semaineApp = ?");
            easy_sales.Pst.setInt(2, Integer.parseInt(CBSemaine.getSelectedItem().toString()));
            easy_sales.Pst.setString(1, lblSite.getText().trim());
            easy_sales.rs = easy_sales.Pst.executeQuery();
            if (easy_sales.rs.next()) {
                Map para = new HashMap();
                para.put("site", lblSite.getText().trim());
                para.put("semaine", Integer.parseInt(CBSemaine.getSelectedItem().toString().trim()));
                JasperPrint jp = JasperFillManager.fillReport(getClass().getResourceAsStream("etats/approSemaineOK.jasper"),para,easy_sales.cn);
                JasperViewer.viewReport(jp, false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur : "+e.getMessage(), "Easy Sales", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


