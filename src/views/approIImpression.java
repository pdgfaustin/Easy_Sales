    void chargerCB(){
        try {
            CBSemaine.removeAllItems();
            easy_sales.connexionEasy();
            easy_sales.Pst = (PreparedStatement) easy_sales.cn.clientPrepareStatement("SELECT DISTINCT semaineApp FROM approSemaine");
            easy_sales.rs = easy_sales.Pst.executeQuery();
            while (easy_sales.rs.next()) {                
                CBSemaine.addItem(easy_sales.rs.getString(1));
            }
        } catch (Exception e) {
            System.err.println("Erreur : "+e.getMessage());
        }
    }
