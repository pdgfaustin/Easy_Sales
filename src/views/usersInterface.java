    void chargerTableUser(){
        try {
            dt = new DefaultTableModel();
            dt.addColumn("ID");
            dt.addColumn("Nom Complet");
            dt.addColumn("Status");
            dt.addColumn("PWD");
            tblMix.setModel(dt);
            tblMix.getColumn("ID").setMaxWidth(100);
            tblMix.getColumn("Status").setMaxWidth(200);
            tblMix.getColumn("PWD").setMaxWidth(0);
            easy_sales.connexionEasy();
            easy_sales.Pst = (ClientPreparedStatement) easy_sales.cn.prepareStatement("SELECT idUsers, nomcomplet, statuts, pwd FROM UsersBD");
            easy_sales.rs = easy_sales.Pst.executeQuery();
            while (easy_sales.rs.next()) {                
                Object a,b,c,d;
                a = easy_sales.rs.getObject(1);
                b = easy_sales.rs.getObject(2);
                c = easy_sales.rs.getObject(3);
                d = easy_sales.rs.getObject(4);
                Object [] e = {a,b,c,d};
                dt.addRow(e);
            }
            tblMix.setModel(dt);
            easy_sales.deconnexionEasy();
        } catch (Exception e) {
            System.err.println("Erreur : "+e.getMessage());
        }
    }
