    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int msg = JOptionPane.showConfirmDialog(this, "Les donnÃ©es sont - elles correctes ?", "Easy Sales", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (msg == JOptionPane.YES_OPTION) {
                boolean vr = verifieZone();
                if (vr) {
                    appelUser(); appelTravail();
                    boolean tr = user.rechercherUser();
                    if (tr) {
                        int msg1 = JOptionPane.showConfirmDialog(this, "Cet'Utilisateur existe dÃ©jÃ , voulez vous le modifier ?", "Easy Sales", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                        if (msg1== JOptionPane.YES_OPTION) {
                            user.modifier();
                            TU.finTravail();
                            TU.enregistrerTravail();
                            JOptionPane.showMessageDialog(this, "Modifications effectuÃ©e", "Easy Sales", JOptionPane.INFORMATION_MESSAGE);
                            nettoie();
                        }
                    }else{
                        user.enregistrer();
                        TU.finTravail();
                        TU.enregistrerTravail();
                        JOptionPane.showMessageDialog(this, "Enregistrement effectuÃ©", "Easy Sales", JOptionPane.INFORMATION_MESSAGE);
                        nettoie();
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Remplissez toutes les zones Obligatoires SVP", "Easy Sales", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur : "+e.getMessage(), "Easy Sales", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

