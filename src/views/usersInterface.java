    boolean verifieZone(){
        boolean vr = false;
        try {
            if (!txtSiteID.getText().equalsIgnoreCase(null) && !txtUserID.getText().equalsIgnoreCase(null) && cbStatus.getSelectedIndex()!= 0) {
                vr = true;
            }
        } catch (Exception e) {
            System.err.println("Erreur : "+e.getMessage());
        }
        return vr;
    }
